package com.proyecto1.ecommerce.domain;

public class Rol {

	private int idRol;
	private String tipo;
	
	public Rol() {
	}

	public int getIdRol() {
		return idRol;
	}
	public void setIdRol(int idRol) {
		this.idRol = idRol;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	
	
}
